package Gun15;

public class _03_forLoop {
    public static void main(String[] args) {
        //0 dan 10 dahil sayıları yazdırın
        // 10 dan 0 dahil sayıları yazdırın

        for(int i=0;i<=10;i++){
            System.out.println("i ="+i);
        }
        for(int j=10;j>=0;j--){
            System.out.println("j :"+j);
        }
    }
}
